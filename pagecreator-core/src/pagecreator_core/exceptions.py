"""Доменные исключения для создания страниц."""


class PageCreatorCoreError(Exception):
    """
    Базовое исключение пакета доменной логики.

    Используется как общий родитель для всех ошибок,
    связанных с разбором путей и построением плана страниц.
    """


class InvalidPathError(PageCreatorCoreError):
    """
    Ошибка пустого или некорректного пути.

    Выбрасывается, если путь не передан, состоит только из
    пробельных символов или после разбора содержит пустые сегменты.
    """


class DuplicateTitleError(PageCreatorCoreError):
    """
    Ошибка повторяющегося названия страницы внутри одного пути.

    Повтор названий внутри одного пути запрещен, чтобы план
    создания страниц был однозначным.
    """
